package com.example;

public class Persegi {

	private int sisi;
	
    // Metode untuk menghitung luas persegi
	public int hitungLuasPersegi(int sisi) {
        return sisi * sisi;
    }

    // Metode untuk menghitung keliling persegi
    public int hitungKelilingPersegi(int sisi) {
        return 4 * sisi;
    }
}
